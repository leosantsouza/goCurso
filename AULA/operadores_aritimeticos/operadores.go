package main

import "fmt"

func main()  {

	valor1 := 10
	valor2 := 5

	// Soma

	soma := valor1 + valor2

	// Subtraçao

	subtracao := valor1 - valor2

	// Multiplicaçao

	multiplicacao := valor1 * valor2

	// Divisao

	divisao := valor1 / valor2

	// Resto da divisao

	resto := valor1 % valor2



	fmt.Println("Soma", soma)
	fmt.Println("Subtraçao", subtracao)
	fmt.Println("Multiplicaçao", multiplicacao)
	fmt.Println("Divisao", divisao)
	fmt.Println("Resto da Divisao", resto)
	fmt.Println("teste")
}